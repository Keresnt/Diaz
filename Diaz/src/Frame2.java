    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String name = NameField.getText();
        String email = EmailField.getText();
        String password = PasswordField.getText();
        String userType = userTypeBox.getSelectedItem().toString();

        // Validate input
        if (name.isEmpty() || email.isEmpty() || password.isEmpty() || userType.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please fill all fields correctly.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Connection conn = null;
        PreparedStatement pstmt = null;

        try {
            // Get database connection
            conn = DatabaseConnection.getConnection();

            // Prepare SQL statement
            String sql = "INSERT INTO users (name, email, password, user_type) VALUES (?, ?, ?, ?)";
            pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, name);
            pstmt.setString(2, email);
            pstmt.setString(3, password); // Consider hashing the password before storing it
            pstmt.setString(4, userType.toLowerCase());

            // Execute the statement
            int rowsAffected = pstmt.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "User  registered successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                // Optionally, clear the fields after successful registration
                NameField.setText("");
                EmailField.setText("");
                PasswordField.setText("");
                userTypeBox.setSelectedIndex(0);
            } else {
                JOptionPane.showMessageDialog(this, "User  registration failed.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            // Close resources
            try {
                if (pstmt != null) pstmt.close();
                if (conn != null) conn.close();
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

